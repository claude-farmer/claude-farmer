export type SpriteData = (string | null)[][];

// ── 캐릭터 스프라이트 (16×16, 아래보기 기본 포즈) ──
const CHAR_MAP: Record<string, string> = {
  H: PALETTE.hair,
  h: PALETTE.hairHighlight,
  S: PALETTE.skin,
  s: PALETTE.skinShadow,
  E: PALETTE.eyes,
  B: PALETTE.blush,
  C: PALETTE.clothes,
  c: PALETTE.clothesShadow,
  P: PALETTE.pants,
  p: PALETTE.pantsShadow,
  O: PALETTE.shoes,
  D: PALETTE.shadow,
};

function parseSprite(rows: string[], colorMap: Record<string, string>): SpriteData {
  return rows.map(row =>
    row.split('').map(ch => (ch === '0' ? null : colorMap[ch] ?? null))
  );
}

